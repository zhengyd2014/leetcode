/**
 * Definition for singly-linked list.
 * type ListNode struct {
 *     Val int
 *     Next *ListNode
 * }
 */
func swapPairs(head *ListNode) *ListNode {
	dummy := &ListNode{}
	dummy.Next = head
	p := dummy
	for p.Next != nil && p.Next.Next != nil {
		first := p.Next
		second := first.Next
		next := second.Next
		second.Next = first
		first.Next = next
		p.Next = second
		p = first
	}
	return dummy.Next
}
