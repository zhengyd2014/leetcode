/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode(int x) { val = x; }
 * }
 */
class Solution {
    public void flatten(TreeNode root) {
        if (root == null) return;
        
        flatten(root.left);
        flatten(root.right);

        TreeNode p = root.left;
        while (p != null && p.right != null) p = p.right;  // important
        if (p != null) {
            p.right = root.right;
            root.right = root.left;    // important
            root.left = null;   
        }
    }
}
